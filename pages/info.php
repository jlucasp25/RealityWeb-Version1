<h2>Utilização do Portal</h2>

<p>Se é um novo utilizador, comece por se registar na página inicial.</p>
<p>Terá de fornecer o seu nome / nome da empresa, um nome de utilizador, um endereço de e-mail, uma senha de acesso (password), um número de telemóvel e opcionalmente, um número de contribuinte.</p>
<p>O número de contribuinte é necessário, apenas se desejar fatura com contribuinte.</p>
<img class="tutorial" src="../custom_assets/img/tut1.png">
<p>Após registar-se, pode entrar usando o nome de utilizador e senha.</p>
<p>A primeira vez que entrar no portal terá de registar pelo menos uma viatura para fazer marcações.</p>
<p>Todas as viaturas que registar ficarão associadas à sua conta, para uso futuro.</p>
<p>Pode também remover uma viatura, no caso de já não a possuir. Se não conseguir registar uma viatura, por favor contacte-nos.</p>
<img class="tutorial" src="../custom_assets/img/tut2.png">
<p>Para fazer marcações de lavagens, pode utilizar o separador "Reservas" para ver as vagas disponíveis e efetuar reservas.</p>
<p>Se necessitar de anular uma lavagem, por favor entre em contacto connosco através dos números acima.</p>
<p>Pode deixar uma mensagem (opcional) ao staff, no campo "Observações" durante o processo de reserva.</p>
<img class="tutorial" src="../custom_assets/img/tut3.png">
<p>Para confirmar a lavagem, verificar o estado da mesma ou ver todas as marcações prévias, pode utilizar o separador "Historial".</p>
<p>Este separador permite saber se a lavagem está a ocorrer no momento ou se já foi finalizada.</p>
<img class="tutorial" src="../custom_assets/img/tut4.png">
